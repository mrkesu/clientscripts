$ErrorActionPreference = "Stop"

$ExitCode = 0

# Must be run with local admin right
$id = [System.Security.Principal.WindowsIdentity]::GetCurrent()
$p = New-Object System.Security.Principal.WindowsPrincipal($id)
if (!$p.IsInRole([System.Security.Principal.WindowsBuiltInRole]::Administrator)) {
    Throw 'This script requires admin rights.'
}

try {
    # Silent uninstall for Logitech Sync
    if (Test-Path -Path "C:\Program Files (x86)\Logitech\LogiSync\uninst-main.exe" ) {
        Write-Host "Uninstalling Logitech Sync..."
        & "C:\Program Files (x86)\Logitech\LogiSync\uninst-main.exe" /S
    } else {
        Write-Host "Logitech Sync Uninstaller not found."
    }

    # This log-file is used by our installation detection method,
    # so we need to make sure it is removed to trigger reinstallation.
    if (Test-Path -Path "C:\Windows\Temp\LogiSync\LogiSyncProvisionStub_Result.txt" ) {
        Write-Host "Removing LogiSyncProvisionStub_Result.txt..."
        Remove-Item -Path "C:\Windows\Temp\LogiSync\LogiSyncProvisionStub_Result.txt"
    } else {
        Write-Host "LogiSyncProvisionStub_Result.txt not found."
    }
}
catch {
    Write-Host $_.Exception.Message
    $ExitCode = 1
}
finally {
    # Clear the intune app "cache" so the intune extension run a full re-evaluation
    Write-Host "Clearing intune app cache..."
    Stop-Service -Name IntuneManagementExtension
    Get-Item -Path registry::HKEY_LOCAL_MACHINE\SOFTWARE\Microsoft\IntuneManagementExtension\win32App* | Remove-Item -Force -Recurse
    Restart-Service -Name IntuneManagementExtension
}

Exit $ExitCode

