# Verify Windows Update for Business settings.
# Should probably add verification for Microsoft update Health Tools: https://support.microsoft.com/en-us/topic/kb4023057-update-for-windows-update-service-components-fccad0ca-dc10-2e46-9ed1-7e392450fb3a

# We want clients to check for monthly updates and feature upgrades.
$required_types = "QualityUpdate", "FeatureUpdate"

# How often should client verify it's policy (minimally)?
$required_days = 14

$exit = 0 # Assume everything is good until it's not.

# For simpler debugging in Intune, nothing there tells us when proactive remediations run
$now = Get-Date -format G

Try
{
    $WufbDS = Get-ItemProperty -Path Registry::HKEY_LOCAL_MACHINE\SOFTWARE\Microsoft\WufbDS
    $enrollmentcheckedon = [datetime]::Parse($WufbDS.enrollmentcheckedon)
    $enrollmenttype = $WufbDS.enrollmenttype.Split(",").Trim()
    $timeago = New-TimeSpan -Start $enrollmentcheckedon -End (Get-Date)

    if($timeago.Days -gt $required_days)
    {
        Write-Output "$now enrollmentcheckedon is old, last check was $($timeago.Days) days ago"
    }

    foreach($item in $required_types)
    {
        if($item -notin $enrollmenttype)
        {
            Write-Output "$now enrollmenttype $item is not set"
            $exit = 1
        }
    }
}
Catch
{
    Write-Output $_.ErrorDetails.Message
    $exit = 1
}
Finally
{
    Exit $exit
}
